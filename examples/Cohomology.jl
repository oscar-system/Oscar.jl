mutable struct CohomologyModule{gT,mT}
  G::gT
  M::mT
  ac::Vector{Map} # automorphisms of M, one for each generator of G

  function CohomologyModule(G::PermGroup, ac::Vector{<:Map})
    r = new{PermGroup,typeof(domain(ac[1]))}()
    r.G = G
    r.ac = ac
    r.M = domain(ac[1])
    @assert all(x -> domain(x) == codomain(x) == r.M, ac)
    return r
  end

  F::Group # G as an Fp-group (if set)
  mF::GAPGroupHomomorphism  # F -> G, maps F[i] to G[i]

  iac::Vector{Map} # the inverses of ac
end

function Base.show(io::IO, C::CohomologyModule)
  print(io, C.G, " acting on ", C.M, "\nvia: ", C.ac)
end

"""
Compute the fp-presentation of the group generated by 'g'
Returns both the group and the map from the new group to the
parent of the generators.
"""
function fp_group(g::Vector{PermGroupElem})
  G = parent(g[1])
  @assert all(x->parent(x) == G, g)
  X = GAP.Globals.IsomorphismFpGroupByGenerators(G.X, GAP.Globals.GeneratorsOfGroup(G.X))
  F = FPGroup(GAP.Globals.Range(X))
  return F, GAPGroupHomomorphism(F, G, GAP.Globals.InverseGeneralMapping(X))
end

"""
For an element of an fp-group, return a corresponding word as a sequence
of integers. A positive integers indicates the corresponding generator, 
a negative one the inverse.
"""
function word(y::FPGroupElem)
  z = GAP.Globals.UnderlyingElement(y.X)
  return map(Int, GAP.Globals.LetterRepAssocWord(z))
end

"""
The relations defining 'F' as an array of pairs.
"""
function relations(F::FPGroup)
  R = relators(F)
  z = one(free_group(F))
  return [(x, z) for x = R]
end

AbstractAlgebra.Group(C::CohomologyModule) = C.G
AbstractAlgebra.Module(C::CohomologyModule) = C.M
action(C::CohomologyModule) = C.ac

function inv_action(C::CohomologyModule)
  if !isdefined(C, :iac)
    C.iac = map(inv, C.ac)
  end
  return C.iac
end

function fp_group(C::CohomologyModule)
  if !isdefined(C, :F)
    C.F, C.mF = fp_group(gens(Group(C)))
  end
  return C.F, C.mF
end

function action(C::CohomologyModule, g, v)
  @assert parent(g) == Group(C)
  @assert parent(v) == Module(C)
  F, mF = fp_group(C)
  ac = action(C)
  iac = inv_action(C)

  for i = word(preimage(mF, g))
    if i > 0
      v = ac[i](v)
    else
      v = iac[-i](v)
    end
  end
  return v
end

