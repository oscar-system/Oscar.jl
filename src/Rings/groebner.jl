"""
function syzygy_generators(a::Vector{<:MPolyRingElem})
  I = ideal(a)
  s = Singular.syz(singular_generators(I))
  F = free_module(parent(a[1]), length(a))
  @assert rank(s) == length(a)
  return [F(s[i]) for i=1:Singular.ngens(s)]
end

