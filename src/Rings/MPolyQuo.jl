@doc Markdown.doc"""
    simplify(a::MPolyQuoIdeal)

Reduce `a` with regard to the modulus of the quotient ring.

    simplify!(a::MPolyQuoIdeal)

Reduce `a` with regard to the modulus of the quotient ring, and replace `a` by the reduction.

CAVEAT: The implementation proceeds by computing a Groebner basis of the quotient ideal first. This may take some time.

# Examples
```jldoctest
julia> R, (x, y) = PolynomialRing(QQ, ["x", "y"], ordering=:degrevlex);

julia> Q, q = quo(R, ideal(R, [x^3*y^2-y^3*x^2, x*y^4-x*y^2]));

julia> a = ideal(Q,[x^3*y^4-x+y, x*y+y^2*x])
MPolyQuoIdeal(x^3*y^4 - x + y, x*y^2 + x*y)

julia> simplify(a);

julia> a
MPolyQuoIdeal(x^3*y^4 - x + y, x*y^2 + x*y)

julia> simplify!(a);

julia> a
MPolyQuoIdeal(x^2*y^3 - x + y, x*y^2 + x*y)
```
"""
function simplify(a::MPolyQuoIdeal)
   R   = base_ring(a)
   oscar_assure(a)
   RI  = base_ring(a.I)
   J = R.I
   groebner_assure(J)
   singular_assure(J.gb)
   oscar_assure(a)
   singular_assure(a.I)
   red  = reduce(a.I.gens.S, J.gb.S)
   SR   = singular_ring(R)
   si   = Singular.Ideal(SR, gens(red))
   red  = MPolyQuoIdeal(R, si)
   return red
end
function simplify!(a::MPolyQuoIdeal)
    R   = base_ring(a)
    oscar_assure(a)
    RI  = base_ring(a.I)
    J = R.I
    groebner_assure(J)
    singular_assure(J.gb)
    oscar_assure(a)
    singular_assure(a.I)
    red  = reduce(a.I.gens.S, J.gb.S)
    SR   = singular_ring(R)
    a.SI = Singular.Ideal(SR, gens(red))
    a.I  = ideal(RI, RI.(gens(a.SI)))
    return a
end

@doc Markdown.doc"""
    issubset(a::MPolyQuoIdeal, b::MPolyQuoIdeal)

Return `true` if `a` is contained in `b`, `false` otherwise.

# Examples
```jldoctest
julia> R, (x, y) = PolynomialRing(QQ, ["x", "y"], ordering=:degrevlex)
(Multivariate Polynomial Ring in x, y over Rational Field, fmpq_mpoly[x, y])

julia> Q, q = quo(R, ideal(R, [x^3*y^2-y^3*x^2, x*y^4-x*y^2]))
(Quotient of Multivariate Polynomial Ring in x, y over Rational Field by ideal generated by: x^3*y^2 - x^2*y^3, x*y^4 - x*y^2, Map from
Multivariate Polynomial Ring in x, y over Rational Field to Quotient of Multivariate Polynomial Ring in x, y over Rational Field by ideal generated by: x^3*y^2 - x^2*y^3, x*y^4 - x*y^2 defined by a julia-function with inverse
)

julia> a = ideal(Q,[x^3*y^4-x+y, x*y+y^2*x])
MPolyQuoIdeal(x^3*y^4 - x + y, x*y^2 + x*y)

julia> b = ideal(Q,[x^3*y^3-x+y, x^2*y+y^2*x])
MPolyQuoIdeal(x^3*y^3 - x + y, x^2*y + x*y^2)

julia> issubset(a,b)
false

julia> issubset(b,a)
true
```
"""
function Base.issubset(a::MPolyQuoIdeal, b::MPolyQuoIdeal)
  base_ring(a) == base_ring(b) || error("base rings must match")
  simplify!(a)
  if !(isdefined(b, :SI))
    simplify!(b)
  end
  if b.SI.isGB == false
    b.SI      = Singular.std(b.SI)
    b.SI.isGB = true
  end
  return Singular.iszero(Singular.reduce(a.SI, b.SI))
end

