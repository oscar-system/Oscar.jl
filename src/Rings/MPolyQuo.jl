@doc Markdown.doc"""    
    ideal(Q::MPolyQuo{T}, V::Vector{T}) where T <: MPolyElem

Given a (graded) quotient ring `Q=R/I` and a vector `V` of (homogeneous) polynomials in `R`, 
create the ideal of `Q` which is generated by the images of the entries of `V`.

    ideal(Q::MPolyQuo{T}, V::Vector{MPolyQuoElem{T}}) where T <: MPolyElem

Given a (graded) quotient ring `Q` and a vector `V` of (homogeneous) elements of `Q`, 
create the ideal of `Q` which is generated by the entries of `V`.
"""
function ideal(Q::MPolyQuo{T}, V::Vector{T}) where T <: MPolyElem
  @assert length(V) > 0
  for p in V
    Q.R == parent(p) || error("parents must match")
  end
  return MPolyQuoIdeal(Q, ideal(Q.R, V))
end
function ideal(Q::MPolyQuo{T}, V::Vector{MPolyQuoElem{T}}) where T <: MPolyElem
  @assert length(V) > 0
  for p in V
    Q == parent(p) || error("parents must match")
  end
  return MPolyQuoIdeal(Q, ideal(Q.R, map(p->p.f, V)))
end

