"""
function Base.:*(I::MPolyIdeal{T}, J::MPolyIdeal{T}) where T
  if base_ring(I) == base_ring(J)
      return MPolyIdeal(unique(vcat([broadcast(*, g, J.gens.O) for g in I.gens.O]...)))
  else
    error("Not possible due to different base rings.")
  end
end

