@doc Markdown.doc"""
    is_monomial(f::MPolyElem)

Return `true` if `f` is a monomial, `false` otherwise.

    is_monomial(I::MPolyIdeal)

Return `true` if `I` can be generated by monomials, `false` otherwise.

# Examples
```jldoctest
julia> R, (x, y) = PolynomialRing(QQ, ["x", "y"])
(Multivariate Polynomial Ring in x, y over Rational Field, fmpq_mpoly[x, y])

julia> f = 2*x+y
2*x + y

julia> g = y
y

julia> is_monomial(f)
false

julia> is_monomial(g)
true

julia> is_monomial(ideal(R, [f, g]))
true
```
"""
function is_monomial(f::MPolyElem)
   if (length(f) == 1 && coeff(f, 1) == 1)
      return true
   else
      return false
   end  
end

function _ismonomial(V::Vector{<: MPolyElem})
  return all(is_monomial, V)
end

function is_monomial(I::MPolyIdeal)
  if _ismonomial(gens(I))
    return true
  end
  GB = groebner_basis(I, complete_reduction = true)
  if _ismonomial(GB)
    return true
  end
  return false
end 

