function vector_space_basis(kk::Field, Q::MPolyQuoRing)
  @assert kk === coefficient_ring(Q)
  I = modulus(Q)
  P = base_ring(I)
  SI = singular_generators(groebner_basis(I))
  sb = Singular.kbase(SI)
  return [P(x) for x in gens(sb)]::Vector{elem_type(P)}
end

function _global_section_module(psi::FiniteGradedMPolyAnyMap, M::SubquoModule)
  @assert base_ring(M) === codomain(psi) "wrong base ring"
  pf_M, interp = restriction_of_scalars(psi, M)
  @assert is_graded(pf_M)
  pf_M_simp, to_pf_M = simplify(pf_M)
  @assert is_graded(pf_M_simp)
  @assert codomain(to_pf_M) === pf_M
  P = domain(psi)
  A = codomain(psi)
  P1 = graded_free_module(P, [0])
  M1, _ = hom(pf_M_simp, P1)
  @assert is_graded(M1)
  M1_simp, to_M1 = simplify(M1)
  @assert is_graded(M1_simp)
  M2, _ = hom(M1_simp, P1)
  @assert is_graded(M2)
  M2_simp, to_M2 = simplify(M2)

  @assert is_graded(M2_simp)
  @assert base_ring(M2_simp) === P
  # recover the A-module structure on M2_simp
  N, to_N = change_base_ring(psi, M2_simp)


  # We first construct the endomorphisms of `M1` given by multiplication
  # with the elements of the `generating_system` of `A` over `P`.
  mult_maps = []
  for x in generating_system(psi)
    img_gens = gens(pf_M_simp)
    img_gens = [preimage(interp, to_pf_M(v)) for v in img_gens]
    img_gens = [x*v for v in img_gens]
    img_gens = [interp(v) for v in img_gens]
    img_gens = [preimage(to_pf_M, v) for v in img_gens]
    f = hom(pf_M_simp, pf_M_simp, img_gens)
    push!(mult_maps, f)
  end

  dual_mult_maps = []
  for f in mult_maps
    img_gens = elem_type(M1_simp)[]
    for g in gens(M1_simp)
      gg = to_M1(g)
      phi = element_to_homomorphism(gg)
      c = compose(f, phi)
      w = homomorphism_to_element(M1, c)
      push!(img_gens, preimage(to_M1, w))
    end
    push!(dual_mult_maps, hom(M1_simp, M1_simp, img_gens))
  end

  mult_tables = Vector{elem_type(M2_simp)}[]
  for a in gens(M2_simp)
    aa = to_M2(a)
    aa_map = element_to_homomorphism(aa)
    row = elem_type(M2_simp)[]
    for h in dual_mult_maps
      g = compose(h, aa_map)
      w = homomorphism_to_element(M2, g)
      push!(row, preimage(to_M2, w))
    end
    push!(mult_tables, row)
  end

  new_relns = elem_type(N)[]
  for (g, row) in zip(gens(M2_simp), mult_tables)
    new_relns = vcat(new_relns, [to_N(xg) - x*to_N(g) for (x, xg) in zip(generating_system(psi), row)])
  end

  NN, to_NN = quo(N, new_relns)
  result, to_result = simplify(NN)

  # construct the canonical map of `M` to the result
  img_gens = gens(M)
  img_gens = [interp(v) for v in img_gens]
  img_gens = [preimage(to_pf_M, v) for v in img_gens]
  img_gens2 = elem_type(N)[]
  for v in img_gens
    vv = [element_to_homomorphism(to_M1(g))(v) for g in gens(M1_simp)]
    h = hom(M1_simp, P1, vv)
    ww = homomorphism_to_element(M2, h)
    www = preimage(to_M2, ww)
    w4 = to_N(www)
    push!(img_gens2, to_NN(w4))
  end
  img_gens3 = elem_type(result)[preimage(to_result, x) for x in img_gens2]
  return result, hom(M, result, img_gens3)
end

@doc raw"""
    co_extension_of_scalars(
        psi::FiniteGradedMPolyAnyMap, N::ModuleFP; 
        domain_as_module=restriction_of_scalars(psi, graded_free_module(domain(psi), [0]))
      )

Given a finite map `φ : A → B` and a `B`-module `N`, compute `M = Hom_B(A, N)` and return a pair `(M, m)` where `m` is a "multiplication map" which turns an element `x` of `B` into the endomorphism of `M` induced by multiplication with `x`. 
"""
function co_extension_of_scalars(
    psi::FiniteGradedMPolyAnyMap, N::ModuleFP;
    domain_as_module=restriction_of_scalars(psi, graded_free_module(domain(psi), [0]))
  )
  B = domain(psi)
  @assert B === base_ring(N) "wrong rings"
  A = codomain(psi)
  FF, interp = domain_as_module
  result, interp_res = hom(FF, N)
  function elem_to_end(x)
    parent(x) === A || error("wrong parent")
    return function(v)
      parent(v) === result || error("wrong parent")
      phi = element_to_homomorphism(v)
      psi = hom(FF, FF, elem_type(FF)[interp(x*preimage(interp, w)) for w in gens(FF)])
      c = compose(psi, phi)
      return homomorphism_to_element(result, c)
    end
  end
  return result, elem_to_end
end

@doc raw"""
     restriction_of_scalars(psi::FiniteGradedMPolyAnyMap, f::ModuleFPHom;
        domain_restriction=restriction_of_scalars(psi, domain(f)),
        codomain_restriction=restriction_of_scalars(psi, codomain(f))
      )

Given a finite map ``φ : A → B`` and a morphism of ``B``-modules ``f : M → N``
return ``φ_* f : φ_* M → φ_* N``, i.e. the induced map on the `restriction_of_scalars` 
along ``φ`` of the domain and the codomain of ``f``.
"""
function restriction_of_scalars(psi::MapType, f::ModuleFPHom;
    domain_restriction=restriction_of_scalars(psi, domain(f)),
    codomain_restriction=restriction_of_scalars(psi, codomain(f))
  ) where {MapType <: Union{FiniteGradedMPolyAnyMap, MPolyAnyMap}}
  M, interp_M = domain_restriction
  N, interp_N = codomain_restriction
  return hom(M, N, elem_type(N)[interp_N(f(preimage(interp_M, x))) for x in gens(M)])
end

function restriction_of_scalars(
    phi::MPolyAnyMap{DomainType, CodomainType, Nothing}, 
    F::FreeMod
  ) where {DomainType <: MPolyRing, CodomainType <: MPolyQuoRing}
  S = domain(phi)
  A = codomain(phi)
  # Should it ever become necessary to widen the functionality to other maps, 
  # a distinction needs to be made here and everything must be handed to internal 
  # methods accordingly. For the moment, this is the case that interests us. 
  @assert base_ring(A) === S && all(phi(x) == y for (x, y) in zip(gens(S), gens(A))) "restriction of scalars is only implemented for projection maps to quotients"
  #pf_F = graded_free_module(S, degrees_of_generators(F))
  pf_F = is_graded(F) ? graded_free_module(S, degrees_of_generators(F)) : free_module(S, ngens(F))
  I = sub_object(pf_F, reduce(vcat, [elem_type(pf_F)[x*g for x in gens(modulus(A))] for g in gens(pf_F)]))
  result = quo_object(pf_F, I)
  return result, MapFromFunc(F, result, x->result(map_entries(lift, coordinates(x))), y->F(map_entries(A, coordinates(y))))
end

function restriction_of_scalars(
    phi::MPolyAnyMap{DomainType, CodomainType, Nothing}, 
    M::SubquoModule;
    ambient_restriction=restriction_of_scalars(phi, ambient_free_module(M))
  ) where {DomainType <: MPolyRing, CodomainType <: MPolyQuoRing}
  pf_F, interp_F = ambient_restriction
  new_gens = elem_type(pf_F)[interp_F(v) for v in ambient_representatives_generators(M)]
  new_rels = elem_type(pf_F)[interp_F(v) for v in relations(M)]
  I = sub_object(pf_F, new_gens)
  result = quo_object(I, new_rels)
  return result, MapFromFunc(M, result, x->result(map_entries(lift, coordinates(x))), y->M(map_entries(codomain(phi), coordinates(y))))
end

